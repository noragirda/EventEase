package com.eventsapp.backend.model.enums;

/**
 * Enum representing the different components of a main course option
 * that can be customized or selected during event planning.
 */
public enum MainCourseOptionTypes {

    /** The primary protein or central item of the main course (e.g., beef, chicken, fish). */
    MAIN_ELEMENT,

    /** Cooked vegetables served as part of the main course. */
    COOKED_VEGETABLES,

    /** Starches such as potatoes, rice, or pasta accompanying the dish. */
    STARCHES,

    /** Salads served alongside the main course. */
    SALADS,

    /** Sauces provided with the main course. */
    SAUCE
}
